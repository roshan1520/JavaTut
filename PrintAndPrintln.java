public class PrintAndPrintln {
    public static void main(String[] args){
        System.out.print("This is the print statementy. It will execute in only the single line.");
        // this is the println statement.
        System.out.println();
        System.out.println("Cursor will move to the the next line and also escape to the new line");
        System.out.println("hello");
        System.out.println("world");
        System.out.println("hello\nworld");
        System.out.println("hello\tworld");
        
    }
}